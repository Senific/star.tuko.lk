export const getTranslation = (key: string, lang: Language): string => {
  const keys = key.split('.');
  let result: any = translations;
  
  for (const k of keys) {
    if (result && result[k]) {
      result = result[k];
    } else {
      return key;
    }
  }
  
  if (result && result[lang]) {
    return result[lang];
  }
  
  return result?.en || key;
};
